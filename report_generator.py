# notifier_email.py
import os
import smtplib
from email.mime.text import MIMEText
from dotenv import load_dotenv

load_dotenv()

def send_email_via_smtp(report_str):
    sender = os.getenv("EMAIL_ADDRESS")
    password = os.getenv("EMAIL_PASSWORD")
    recipient = os.getenv("RECIPIENT")

    msg = MIMEText(report_str)
    msg['Subject'] = 'ğŸ“ˆ NVIDIA Daily Investment Report'
    msg['From'] = sender
    msg['To'] = recipient

    try:
        with smtplib.SMTP_SSL('smtp.gmail.com', 465) as server:
            server.login(sender, password)
            server.sendmail(sender, recipient, msg.as_string())
        print("âœ… Email sent.")
    except Exception as e:
        print("âŒ Email failed:", e)


# notifier_slack.py
import os
import requests
from dotenv import load_dotenv

load_dotenv()

def send_slack_message(report_str):
    webhook_url = os.getenv("SLACK_WEBHOOK_URL")
    payload = {"text": report_str}

    try:
        res = requests.post(webhook_url, json=payload)
        if res.status_code == 200:
            print("âœ… Slack message sent.")
        else:
            print("âŒ Slack failed:", res.text)
    except Exception as e:
        print("âŒ Slack error:", e)


